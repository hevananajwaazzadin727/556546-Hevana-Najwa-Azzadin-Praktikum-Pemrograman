#include <iostream>
using namespace std;

int main() { 
    int a = 10; 
    
    // Penjumlahan
    a += 5; 
    cout << "a += 5 : " << a << endl; 
    
    // Pengurangan
    a -= 3;  
    cout << "a -= 3 : " << a << endl; 
    
    // Perkalian
    a *= 2;  
    cout << "a *= 2 : " << a << endl; 
    
    // Pembagian integer
    a /= 4;  
    cout << "a /= 4 : " << a << endl; 
    
    // Modulus
    a %= 3;  
    cout << "a %= 3 : " << a << endl; 
    
    return 0; 
}
